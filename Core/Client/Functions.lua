function Func:CheckDist(PID, Coords, Dist)
  local C = GetEntityCoords(PID);
  local D = Vdist(C.x, C.y, C.z, Coords.x, Coords.y, Coords.z);
  if (D <= Dist) then return true; end;
  return false
end;
